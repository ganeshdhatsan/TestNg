package BasicConcepts;

public class WhatIsFramwork {
/**
 * Framework:
 * It is a structured approach in software testing 
 * It explains how tests are written, executed, and reported
 */
	
	/**
	 * Types of Framework in Industry
	 * 
	 * 1.TDD (Test-Driven Development) -- TestNG,Junit
	 * 2.BDD (Behavior-Driven Development) -- Cucumber
	 * 
	 * HYBRID Framework:(TDD+BDD)
	 * 1.Cucumber (Cucumber+TestNG+Junit) - Hybrid Framework
	 * 2.TestNG  (Cucumber+TestNG) - Hybrid Framework
	 * 3.Junit  (Cucumber+Junit) - Hybrid Framework
	 * 4.Robot
	 * 
	 */
	
	/**
	 * TDD (Test-Driven Development):
	 * Test-Driven Development (TDD) is a software development methodology.
	 * Test cases will be written before the actual code written. 
	 * The goal of TDD is to ensure that the function works as expected by writing enough code.
	 */
	
	/**
	 * BDD (Behavior-Driven Development):
       BDD is an extension of TDD and it focuses on the behavior of the system from a user’s perspective.
       BDD uses simple, natural language to describe the behavior of an application,
       making it easier to understand for both technical and non-technical stakeholders 
       (such as developers, testers, and business analysts).
       The goal is to ensure that the software meets the user’s needs and expectations.
	 */
	
	/**
	 * TestNG:
	 * 1.TestNG is a testing framework for Java
	 * 2.The "NG" in TestNG stands for "Next Generation
	 * 3.It is more powerful and flexible than JUnit
	 * 4.It is used primarily for unit testing, integration testing, functional testing,
	 *  and end-to-end testing in Java applications.
	 * 5.Parallel Execution,Parameterized Testing,Grouping of Tests,Test Reports,Data-Driven Testing
	 *  Exception Testing: You can specify that a particular test method is expected to throw a certain type of exception,
	 *  making it easier to test for exceptions in code.
        Example: @Test(expectedExceptions = ArithmeticException.class)
	 */
	
	/**
	 * Cucumber:
	 * Cucumber is a popular Behavior-Driven Development (BDD) framework 
	 * It define application behavior in plain,Human-readable english language.
	 * Cucumber enables the automation of acceptance tests, 
	 * ensuring that an application behaves as expected from the business users or stakeholders perspective.
	 * Cucumber tests are typically written in a Gherkin language, which is simple and structured,
	 * allowing collaboration between technical and non-technical.
	 * Code Reusability
	 */
	
	
	
	
}
